
import java.util.Scanner;

public class NestedCmplx {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        for (int i = 0; i < n; i++) {

            // INEER LOOP
            for (int j = 0; j < n; j++) {
                System.out.println("Helllo Sir");
            }
        }
        sc.close();
    }
}
